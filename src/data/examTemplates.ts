// ── Helper: Get all unique topics for a country ──
export function getTopicsForCountry(countryId: string): string[] {
  const topics = new Set<string>();
  mockQuestions
    .filter((q) => q.countryId === countryId)
    .forEach((q) => topics.add(q.topic));
  mockExamTemplates
    .filter((t) => t.countryId === countryId)
    .flatMap((t) => t.sections)
    .forEach((s) => s.topicFilterJson?.forEach((t) => topics.add(t)));
  return Array.from(topics).sort();
}
